"""
Operator    Name       Example
   +        Additon    x+y
   -   Substraction    x-y
   *   Multiplication  x*y
   /   Division        x/y
   %   Modulus         x%y
   **   Exponents      x**y
   //   Floor division x//y
"""
a = 5
b = 2
print(a + b)
print(b - a)
print(a * b)
print(a / b)
print(a % b)
print(5 ** 2)  # 5*5 //exponenent use for finding square root and power
print(5 ** 3)  # 5*5*5
print(10 // 3)  # int value given from float means removed the decimal

"""Operator     Example      Same As
    =            x=5          x=5
   +=           x+=3         x=x+3
   -=           x-=3         x=x-3
"""

x = 5
print(x)

x = x+5
print(x)

x += 5
print(x)

